#ifndef W_DTIME_H
#define W_DTIME_H

#include <windows.h>

#include <time.h>
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
void sleep_ms( u_int ms )
    {
    Sleep( ms );
    }
//-----------------------------------------------------------------------------
tm get_time()
    {
    static time_t t_;
    static struct tm timeInfo_;
    t_ = time( 0 );
    localtime_s( &timeInfo_, &t_ );

    return timeInfo_;
    }
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------

#endif // W_DTIME_H
