#include "console.h"
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
void print_binary(unsigned int c) {
  if (0 == c) {
    printf("0");
    return;
  }

  u_int_4 mask = 0x80000000L;
  while (!(c & mask)) mask >>= 1;
  while (mask) {
    printf("%d", (c & mask) > 0 ? 1 : 0);
    mask >>= 1;
  }
}
